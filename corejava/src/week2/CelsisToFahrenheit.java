package week2;

public class CelsisToFahrenheit {

	public static void main(String[] args) {
		
		final double Celsius=37;
		double Fahrenheit;
		
		Fahrenheit=(Celsius*1.8)+32; 
		
		System.out.println("Converting the temp celsis into fahrenheit");
		System.out.println("Celsius temp is "+ Celsius+" Converting in Fahrenheit temp is "+ Fahrenheit);
		 

	}
}
